     */
    public function store(Request $request)
    {

        $title = $request->title;
        $picture = $request->picture;
        $user = Auth::user();
        $bookmark = new Bookmark;
        $bookmark->title = $title;
        $bookmark->user_id = $user->id;
        $bookmark->url = $picture;
        $bookmark->save();

        return redirect('/');
    }

